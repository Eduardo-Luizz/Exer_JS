/*Para criar nomes
    *JS é case sensitive (sensível ao caso)
    *JS aceita a cadeia de caracteres Unicode

__Posso:
    *Iniciar com esses caracteres especiais: $ ou _
    *Iniciar com letras
    *Colocar acentos
    *Letras maíusculas e minúsculas fazem diferença

__Não Posso:
    *Iniciar com números
    *Colocar espaços vazios no nome

__Ideal:
    *Criar nomes que fazem sentido
    *Que explique o que a variávelé ou faz
    *camelCase
    *snake_case
    *Escrever em inglês
*/